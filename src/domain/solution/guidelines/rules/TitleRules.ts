/**
 * Validation rules for guideline title field.
 */

import {
  ValidationRule,
  ValidationResult,
} from "../../../shared/validation/ValidationRule.js";
import {
  GuidelineErrorMessages,
  GuidelineLimits,
  formatErrorMessage,
} from "../Constants.js";

export class TitleRequiredRule implements ValidationRule<string> {
  validate(title: string): ValidationResult {
    const isValid = !!(title && title.trim() !== "");
    return {
      isValid,
      errors: isValid ? [] : [GuidelineErrorMessages.TITLE_REQUIRED],
    };
  }
}

export class TitleMaxLengthRule implements ValidationRule<string> {
  constructor(
    private maxLength: number = GuidelineLimits.TITLE_MAX_LENGTH
  ) {}

  validate(title: string): ValidationResult {
    const isValid = title.length <= this.maxLength;
    return {
      isValid,
      errors: isValid
        ? []
        : [
            formatErrorMessage(GuidelineErrorMessages.TITLE_TOO_LONG, {
              max: this.maxLength,
            }),
          ],
    };
  }
}

export const TITLE_RULES = [
  new TitleRequiredRule(),
  new TitleMaxLengthRule(),
];
